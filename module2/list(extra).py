#insert
fruits = ['apple', 'banana', 'cherry']
fruits.insert(1, "orange")
print(fruits)

#pop	
fruits = ['apple', 'banana', 'cherry']
fruits.pop(1)
print(fruits)
#remove()	
fruits = ['apple', 'banana', 'cherry']
fruits.remove("banana")
print(fruits)
#reverse()	
fruits = ['apple', 'banana', 'cherry']
fruits.reverse()
print(fruits)
#sort()	
cars = ['Ford', 'BMW', 'Volvo']
cars.sort()
print(cars)

